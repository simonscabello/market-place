    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\RedirectResponse
     */
    public function destroy($id)
    {
        $user = User::find($id);

        if ($user->store) {
            $user->store->delete();
        }

        $user->delete();

        return redirect()->route('admin.owners.index');

    }
